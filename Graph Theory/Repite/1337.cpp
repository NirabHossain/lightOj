#include<bits/stdc++.h>
using namespace std;

int M,N,dx[]={1,-1,0,0},dy[]={0,0,1,-1},cost[505][505],ic;
char ch[505][505];
bool vis[505][505];

void dfs(int x1, int y1)
{
    vis[x1][y1]=true;
    for(int i=0;i<4;i++){
        int x=x1+dx[i],y=y1+dy[i];
        if(vis[x][y]||x<0||x>=M||y<0||y>=N||ch[x][y]=='#')continue;
        if(ch[x][y]=='C')ic++;
        dfs(x,y);
    }
}

int main()
{
    int i,j,t,T=1,q,x,y;
    for(cin>>t;t--;){
        cout<<"Case "<<T++<<":\n";
        cin>>M>>N>>q;
        for(i=0;i<M;i++)cin>>ch[i];
        memset(cost,-1,sizeof cost);
        while(q--){
            cin>>x>>y;
            ic=0;memset(vis,0,sizeof vis);
            if(cost[x-1][y-1]!=-1)cout<<cost[x-1][y-1]<<endl;
            else{
                dfs(x-1,y-1);
                cout<<ic<<endl;
                for(i=0;i<M;i++)for(j=0;j<N;j++)if(vis[i][j])cost[i][j]=ic;
            }
        }
    }
}


